from openpyxl import Workbook, load_workbook
from openpyxl.utils import get_column_letter


wb = load_workbook('SPEED.xlsx')
ws = wb.active
Resultado_suma = 0

#MULTIPLICATE CELLS BY 2 BETWEEN CELL 2 AND CELL 40980
for row in range(2, 40981):
    for col in [2]:
        char = get_column_letter(col)
        Resultado_suma += (ws[char + str(row)].value) 

#GET INTEGRATION LIMITS AND DETERMINATE DELTA X
Delta_x = (ws["A40981"].value - ws["A2"].value) / 40980

Resultado_final = (Resultado_suma * Delta_x)
print(Resultado_final)

